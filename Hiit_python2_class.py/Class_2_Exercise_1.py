"""
Write a program
Ask the user for three numbers
Display Sum, Difference and product
""" 

number_1 =int(input("Enter a number: "))
number_2 =int(input("Enter a second number: "))
number_3 =int(input("Enter a third number: "))
print(f"The sum of {number_1} and {number_2} is {number_1 + number_2 + number_3}.")
print(f"The difference of {number_1} and {number_2} is {number_1 - number_2 - number_3}.")
print(f"The product of {number_1} and {number_2} is {number_1 * number_2 * number_3}.")